public class SystemNotificationInfoAction {
    private String jump;

    private int redDot;

    public SystemNotificationInfoAction(String jump, int redDot) {
        this.jump = jump;
        this.redDot = redDot;
    }

    public String getJump() {
        return jump;
    }

    public int getRedDot() {
        return redDot;
    }

}
